async function vaultRewardSetAPR(signer, value) {
  // Create contract instance from the relayer signer
  const vaultReward = new ethers.Contract('0xe12C6aBe5C12eBf29093A8bc8336053FeAdAFFBd', vaultRewardABI, signer);
  const tx = await vaultReward.setAPR(value);
  return tx;
}

async function vaultRewardSetTokensPerInterval(signer, value) {
  // Create contract instance from the relayer signer
  const vaultReward = new ethers.Contract('0xaDA61A37A7F95A8e15510352cA714241558d67a9', rewardDistributorABI, signer);
  const tx = await vaultReward.setTokensPerInterval(value);
  return tx;
}

exports.handler = async function (credentials) {
  // Initialize defender relayer provider and signer
  const provider = new DefenderRelayProvider(credentials);
  const signer = new DefenderRelaySigner(credentials, provider, { speed: 'fast' });
  await vaultRewardSetAPR(signer, '1428970'); // 8 decimal
  return await vaultRewardSetTokensPerInterval(signer, '30'); // 6 decimals
};
